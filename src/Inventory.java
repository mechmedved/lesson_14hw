public class Inventory {
    String type;
    Integer size;
    String color;

    public Inventory(String type, Integer size, String color) {
        this.type = type;
        this.size = size;
        this.color = color;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
